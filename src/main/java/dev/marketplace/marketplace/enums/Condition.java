package dev.marketplace.marketplace.enums;

public enum Condition {
    NEW,               // Brand new, never used
    LIKE_NEW,          // Opened but barely used
    EXCELLENT,         // Minimal wear, fully functional
    GOOD,              // Some signs of use but no major issues
    FAIR,              // Noticeable wear but still works fine
    HEAVILY_USED,      // Significant wear, but still functional
    NEEDS_REPAIR,      // Not fully functional, may require fixing
    FOR_PARTS          // Not working, only useful for parts
}

